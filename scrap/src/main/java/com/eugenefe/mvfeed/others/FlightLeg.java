package com.eugenefe.mvfeed.others;

import java.util.List;

public class FlightLeg {
	private int days;
	private List<FlightHop> hops;
	
	
	private FlightLeg() {
	}


	


	

	public int getDays() {
		return days;
	}







	







	public void setDays(int days) {
		this.days = days;
	}







	public List<FlightHop> getHops() {
		return hops;
	}


	public void setHops(List<FlightHop> hops) {
		this.hops = hops;
	}
	
	
	
	
}
