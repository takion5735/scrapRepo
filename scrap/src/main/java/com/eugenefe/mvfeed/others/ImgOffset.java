package com.eugenefe.mvfeed.others;

public class ImgOffset {

	private double x;
	private double y;
	
	
	
	public ImgOffset() {
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	
	
}
